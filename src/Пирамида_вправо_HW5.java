public class Пирамида_вправо_HW5 {
    public static void main(String[] args) {
        for (int i = 9; i >= 0; i--) {
            // Пробелы для выравнивания пирамиды
            for (int j = 0; j <= i; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
    }
}}

