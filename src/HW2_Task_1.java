public class HW2_Task_1 {
    public static void main(String[] args) {
        int a = 20; // объявление и инициализация переменной
        int b = 2;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        if ((a % 2) == 0)
        {System.out.println("Number is even");}
        else
        {System.out.println("Number is odd");}
    }
}
